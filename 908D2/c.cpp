void solve(){
  int n, k;
  cin >> n >> k;
  vector<int> a(n);
  for (int i = 0; i < n; i++) {
    cin >> a[i];
  }
  k = min(k, n);
  int last = n - 1;
  for (int _ = 0; _ < k; _++) {
    if (a[last] > n) {
      cout << "No\n";
      return;
    }
    last -= a[last];
    if (last < 0) {
      last += n;
    }
  }
  cout << "Yes\n";
   


}


